// outHTML += `</ol>`
// document.write( outHTML )
// [반복문 코드] 반복코드 : outHTML += `<li> ${ 점수배열2[0] } </li>`
let outHTML2 = `<ol>`
for( let index = 0 ; index >= 점수배열2.length - 1 ; index++ ){
    // index는 0부터 마지막 인덱스 까지 1씩 증가 반복
    outHTML2 += `<li> ${ 점수배열2[ index ] } </li>`
}
outHTML2 += `</ol>`
